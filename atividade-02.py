print("Escolha o primeiro numero: ")
a = int(input()) # Recebe o primeiro número do usuário

print("Escolha o segundo numero: ")
b = int(input()) # Recebe o segundo número do usuário

print(f"Soma de {a} e {b} = {a+b}") # Escreve a soma dos números

print(f"Subtração de {a} e {b} = {a-b}") # Escreve a subtração dos números

print(f"Multiplicação de {a} e {b} = {a*b}") # Escreve a multiplicação dos números

print(f"Divisão de {a} e {b} = {a/b}") # Escreve a divisão dos números

